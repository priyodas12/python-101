s3="random letter for traverse!"
# string traverse

# with for loop: you just need each character and don't care about the index.
for x in s3:
    print(x)

# range function
# You need the index, for example:
    # Modifying characters (in a list version of the string).
    # Comparing with neighboring characters.
    # Tracking position.

for y in range(0,len(s3)):
    print(s3[y])
